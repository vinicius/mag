package core.brakes.de.fub.bytecode.generic;

/** 
 * ASTORE - Store reference into local variable
 * Stack ..., objectref -> ... 
 *
 * @version $Id: ASTORE.java,v 1.2 2006-03-11 03:19:48 rafaelf Exp $
 * @author  <A HREF="http://www.inf.fu-berlin.de/~dahm">M. Dahm</A>
 */
public class ASTORE extends LocalVariableInstruction {
	private static final long serialVersionUID = -8812969794706990882L;
	
	/**
	 * Empty constructor needed for the Class.newInstance() statement in
	 * Instruction.readInstruction(). Not to be used otherwise.
	 */
	ASTORE() {
		super(ASTORE, ASTORE_0);
	}  
	public ASTORE(int n) {
		super(ASTORE, ASTORE_0, n);
	}  
}